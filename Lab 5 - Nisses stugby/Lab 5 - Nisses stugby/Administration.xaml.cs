    public partial class Administration : Page
    {
        Labb5Entities dataEntities = new Labb5Entities();
        public Administration()
        {
            InitializeComponent();
            PeopleDatagrid.ItemsSource = dataEntities.People.ToList();
        }

        private void PeopleDatagrid_AutoGeneratedColumns(object sender, EventArgs e)
        {
            PeopleDatagrid.Columns[0].Visibility = Visibility.Hidden;
        }

        //Commits the edits of a row to the database
        private void PeopleDatagrid_RowEditEnding(object sender, DataGridRowEditEndingEventArgs e)
        {
            var i = e.Row.GetIndex();
            if (PeopleDatagrid.SelectedItem != null)
            {
                PeopleDatagrid.RowEditEnding -= PeopleDatagrid_RowEditEnding;
                PeopleDatagrid.CommitEdit();
                PeopleDatagrid.Items.Refresh();
                PeopleDatagrid.RowEditEnding += PeopleDatagrid_RowEditEnding;
            }
            Person editedRow = (Person)PeopleDatagrid.Items[i];
            Person person = dataEntities.People.Find(editedRow.Id);
            person = editedRow;
            dataEntities.SaveChanges();
        }

        private void PeopleDatagrid_PreviewKeyDown(object sender, KeyEventArgs e)
        {
            if (e.Key == Key.Delete)
            {
                if (PeopleDatagrid.SelectedItem != null)
                {
                    Person selectedPerson = dataEntities.People.Find(((Person)PeopleDatagrid.SelectedItem).Id);
                    dataEntities.People.Remove(selectedPerson);
                    dataEntities.SaveChanges();
                }
            }
        }

        private void BtnHome_click(object sender, RoutedEventArgs e)
        {
            Home home = new Home();
            this.NavigationService.Navigate(home);
        }

        private void Registration_Click(object sender, RoutedEventArgs e)
        {
            Registration registration = new Registration();
            this.NavigationService.Navigate(registration);
        }

        private void Search_Click(object sender, RoutedEventArgs e)
        {
            CabinSearch cabinSearch = new CabinSearch();
            this.NavigationService.Navigate(cabinSearch);
        }

        private void Admin_Click(object sender, RoutedEventArgs e)
        {
            Administration administration = new Administration();
            this.NavigationService.Navigate(administration);
        }

        private void Contact_Click(object sender, RoutedEventArgs e)
        {
            ContactInfo contactinfo = new ContactInfo();
            this.NavigationService.Navigate(contactinfo);
        }
    }
